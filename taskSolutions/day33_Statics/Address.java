package day33_Statics;

public class Address {
    public String street, city, state;
    public int zipCode;
    public static String country="Poland", planet="Earth";

    public Address(String street, String city, int zipCode) {
        this.street = street;
        this.city = city;
        this.state = state;
        this.zipCode = zipCode;
    }

    @Override
    public String toString() {
        return "Address{" +
                "country='" + country + '\'' +
                "planet='" + planet + '\'' +
                "street='" + street + '\'' +
                ", city='" + city + '\'' +
                ", state='" + state + '\'' +
                ", zipCode=" + zipCode +
                '}';
    }
}

